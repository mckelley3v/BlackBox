#include "vku_utility.hpp"
#include <cstring>

// ====================================================================================================================
// Implementation
// ====================================================================================================================

std::size_t vku::string_length(char const *str)
{
    return (str != nullptr) ? std::strlen(str) : 0;
}

// --------------------------------------------------------------------------------------------------------------------

std::size_t vku::string_length(std::string const &str)
{
    return str.length();
}

