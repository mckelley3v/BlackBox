#include "m1/crc32_builder.hpp"

// ====================================================================================================================

/*virtual*/ void m1::crc32_builder::append(code_point const ch) /*override*/
{
    utf8_crc32_iterator itr(/*ref*/ m_Crc);
    *itr = ch;
    ++itr;
}

// --------------------------------------------------------------------------------------------------------------------

m1::crc32 m1::crc32_builder::crc() &&
{
    crc32 result = m_Crc;
    m_Crc = crc32();
    return result;
}

