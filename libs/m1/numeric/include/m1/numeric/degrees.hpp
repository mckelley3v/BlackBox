#ifndef M1_NUMERIC_DEGREES_HPP
#define M1_NUMERIC_DEGREES_HPP

namespace m1
{
    // =================================================================================================================

    constexpr float degrees(float radians) noexcept;
    constexpr double degrees(double radians) noexcept;

    // =================================================================================================================
} // namespace m1


// =====================================================================================================================

constexpr float m1::degrees(float const radians) noexcept
{
    return radians * 57.295779513082320876798154814105f;
}

// ---------------------------------------------------------------------------------------------------------------------

constexpr double m1::degrees(double const radians) noexcept
{
    return radians * 57.295779513082320876798154814105;
}

// =====================================================================================================================

#endif // M1_NUMERIC_DEGREES_HPP
