#ifndef M1_NUMERIC_RADIANS_HPP
#define M1_NUMERIC_RADIANS_HPP

namespace m1
{
    // =================================================================================================================

    constexpr float radians(float degrees) noexcept;
    constexpr double radians(double degrees) noexcept;

    // =================================================================================================================
} // namespace m1

// =====================================================================================================================

constexpr float m1::radians(float const degrees) noexcept
{
    return degrees * 0.017453292519943295769236907684886f;
}

// ---------------------------------------------------------------------------------------------------------------------

constexpr double m1::radians(double const degrees) noexcept
{
    return degrees * 0.017453292519943295769236907684886;
}

// =====================================================================================================================

#endif // M1_NUMERIC_RADIANS_HPP
