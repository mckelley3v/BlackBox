#include "m1/numeric/sign.hpp"
#include "m1/numeric/is_small.hpp"

// =====================================================================================================================

int m1::sign(int const x)
{
    return (x == 0) ?  0 :
           (x < 0)  ? -1 : +1;
}

// ---------------------------------------------------------------------------------------------------------------------

float m1::sign(float const x)
{
    return is_small(x) ?  0.0f :
           (x < 0.0f)  ? -1.0f : +1.0f;
}

// ---------------------------------------------------------------------------------------------------------------------

double m1::sign(double const x)
{
    return is_small(x) ?  0.0 :
           (x < 0.0)   ? -1.0 : +1.0;
}

// =====================================================================================================================
