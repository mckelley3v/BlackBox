#include "m1/numeric/step.hpp"

// =====================================================================================================================

float m1::step(float const edge, float const x)
{
    return (x < edge) ? 0.0f : 1.0f;
}

// ---------------------------------------------------------------------------------------------------------------------

double m1::step(double const edge, double const x)
{
    return (x < edge) ? 0.0 : 1.0;
}

