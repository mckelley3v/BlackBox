#include "m1/numeric/min.hpp"

// =====================================================================================================================

int m1::min(int const lhs, int const rhs)
{
    return (lhs < rhs) ? lhs : rhs;
}

// ---------------------------------------------------------------------------------------------------------------------

float m1::min(float const lhs, float const rhs)
{
    return (lhs < rhs) ? lhs : rhs;
}

// ---------------------------------------------------------------------------------------------------------------------

double m1::min(double const lhs, double const rhs)
{
    return (lhs < rhs) ? lhs : rhs;
}

