#include "m1/numeric/is_finite.hpp"
#include <cmath>

// =====================================================================================================================

bool m1::is_finite(int const /*value*/) noexcept
{
    return true;
}

// ---------------------------------------------------------------------------------------------------------------------

bool m1::is_finite(float const value) noexcept
{
    return std::isfinite(value);
}

// ---------------------------------------------------------------------------------------------------------------------

bool m1::is_finite(double const value) noexcept
{
    return std::isfinite(value);
}

