#include "m1/numeric/log.hpp"
#include <cmath>
#include <cassert>

// =====================================================================================================================

float m1::log(float const x)
{
    assert(x > 0.0f);
    return std::log(x);
}

// ---------------------------------------------------------------------------------------------------------------------

double m1::log(double const x)
{
    assert(x > 0.0);
    return std::log(x);
}

// =====================================================================================================================
