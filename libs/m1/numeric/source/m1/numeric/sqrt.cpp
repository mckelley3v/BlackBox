#include "m1/numeric/sqrt.hpp"
#include <cmath>
#include <cassert>

// =====================================================================================================================

float m1::sqrt(float const x)
{
    assert(x >= 0.0f);
    return std::sqrt(x);
}

// ---------------------------------------------------------------------------------------------------------------------

double m1::sqrt(double const x)
{
    assert(x >= 0.0);
    return std::sqrt(x);
}

// =====================================================================================================================
