#include "m1/numeric/lerp.hpp"
#include <cassert>

// =====================================================================================================================

float m1::lerp(float const x0,
               float const x1,
               float const t)
{
    assert(x0 < x1);
    return x0 + t * (x1 - x0);
}

// ---------------------------------------------------------------------------------------------------------------------

double m1::lerp(double const x0,
                double const x1,
                double const t)
{
    assert(x0 < x1);
    return x0 + t * (x1 - x0);
}

