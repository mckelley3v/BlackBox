#include "m1/numeric/is_inf.hpp"
#include <cmath>

// =====================================================================================================================

bool m1::is_inf(int const /*value*/) noexcept
{
    return false;
}

// ---------------------------------------------------------------------------------------------------------------------

bool m1::is_inf(float const value) noexcept
{
    return std::isinf(value);
}

// ---------------------------------------------------------------------------------------------------------------------

bool m1::is_inf(double const value) noexcept
{
    return std::isinf(value);
}

