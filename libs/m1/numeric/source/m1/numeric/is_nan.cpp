#include "m1/numeric/is_nan.hpp"
#include <cmath>

// =====================================================================================================================

bool m1::is_nan(int const /*value*/) noexcept
{
    return false;
}

// ---------------------------------------------------------------------------------------------------------------------

bool m1::is_nan(float const value) noexcept
{
    return std::isnan(value);
}

// ---------------------------------------------------------------------------------------------------------------------

bool m1::is_nan(double const value) noexcept
{
    return std::isnan(value);
}

