#include "m1/numeric/is_nan.hpp"

// =====================================================================================================================

bool m1::is_nan(int const value)
{
    return false;
}

// ---------------------------------------------------------------------------------------------------------------------

bool m1::is_nan(float const value)
{
    return value != value;
}

// ---------------------------------------------------------------------------------------------------------------------

bool m1::is_nan(double const value)
{
    return value != value;
}

