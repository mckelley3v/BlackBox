#include "m1/numeric/modf.hpp"
#include <cmath>

// =====================================================================================================================

float m1::modf(float const x,
               float * const int_part) noexcept
{
    return std::modf(x, int_part);
}

// ---------------------------------------------------------------------------------------------------------------------

double m1::modf(double const x,
                double * const int_part) noexcept
{
    return std::modf(x, int_part);
}

