#include "m1/numeric/round.hpp"
#include <cmath>

// =====================================================================================================================

float m1::round(float const x) noexcept
{
    return std::floor(x + 0.5f);
}

// ---------------------------------------------------------------------------------------------------------------------

double m1::round(double const x) noexcept
{
    return std::floor(x + 0.5);
}

// =====================================================================================================================
