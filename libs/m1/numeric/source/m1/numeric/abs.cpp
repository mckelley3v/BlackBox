#include "m1/numeric/abs.hpp"
#include <cstdlib>
#include <cmath>

// =====================================================================================================================

int m1::abs(int const x)
{
    return std::abs(x);
}

// ---------------------------------------------------------------------------------------------------------------------

float m1::abs(float const x)
{
    return std::fabs(x);
}

// ---------------------------------------------------------------------------------------------------------------------

double m1::abs(double const x)
{
    return std::fabs(x);
}

// =====================================================================================================================
