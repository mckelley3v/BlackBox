#include "m1/numeric/acos.hpp"
#include <cmath>
#include <cassert>

// =====================================================================================================================

float m1::acos(float const x) noexcept
{
    assert(x >= -1.0f);
    assert(x <= +1.0f);
    return std::acos(x);
}

// ---------------------------------------------------------------------------------------------------------------------

double m1::acos(double const x) noexcept
{
    assert(x >= -1.0);
    assert(x <= +1.0);
    return std::acos(x);
}

// =====================================================================================================================
